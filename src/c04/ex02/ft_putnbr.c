#include <unistd.h>

void	print(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	int	mod;
	int	n;

	mod = 0;
	n = nb;
	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		print('-');
		n = n * -1;
	}
	if (n < 10)
		print(n + '0');
	else
	{
		mod = n % 10 + '0';
		ft_putnbr(n / 10);
		print(mod);
	}
}
