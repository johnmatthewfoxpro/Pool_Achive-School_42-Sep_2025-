int	ft_str_is_uppercase(char *str)
{
	int	i;
	int	x;

	i = 0;
	x = 0;
	while (str[i])
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
		{
			x++;
		}
		i++;
	}
	if (i == 0 || i == x)
		return (1);
	else
		return (0);
}
