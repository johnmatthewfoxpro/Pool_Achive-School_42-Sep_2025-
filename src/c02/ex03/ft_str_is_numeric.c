int	ft_str_is_numeric(char *str)
{
	int	i;
	int	x;

	i = 0;
	x = 0;
	while (str[i])
	{
		if (str[i] >= '0' && str[i] <= '9')
		{
			x++;
		}
		i++;
	}
	if (i == 0 || i == x)
		return (1);
	else
		return (0);
}
